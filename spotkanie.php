<h3>Spotkanie integracyjne Koła</h3>
<span class="data">Autor: Dajana Olech, 24 listopada 2014 r.</span><br><br>

<p>
Na spotkanie przybyli niemal wszyscy członkowie Koła i już od pierwszych minut toczyła się zażarta bitwa o przesunięcie swojego pionka o oczko. Pierwsza planszówka, jaka poszła "w ruch" okazała się grą, w której liczyły się wiedza i refleks - w ciągu pięciu sekund trzeba było odpowiedzieć na różnego typu pytania. Każdy, kto obserwował graczy mógł sporo dowiedzieć się o otaczającym świecie, np., że w Dani jest ruch lewostronny,  a jednym z rodzajów torebki jest "ta taka, na ten!".  Mało wie również ten, kto nie wie, że rodzajem zielonej rośliny jest "bźdźło", znaną lekkoatletką - Usain Bolt, jeden ze znanych ojców filmowych - Ojciec Mateusz, a trzy rodzaje metali to: ciężkie, lekkie i Nergal.  W czasie gry objawiało się także nasze "zboczenie zawodowe", gdyż przy pytaniu, o rzeczy na 'o', pierwsze słowo, jakie padło to "obserwatorium", a kiedy trzeba było wymienić trzy dowolne tytuły książek, to bez wahania padło "Analiza tensorowa"! W momencie, kiedy ci, co oszukiwali doszli do mety, ustalono, że trzeba jeszcze wrócić na start.
</p>
<br>
<p>
Kiedy skończyły się pytania, część kołowiczów poszła na podbój mikrofonu. śmiałków nie zraziła nawet awaria monitora wyświetlającego słowa piosenki już po kilku sekundach od rozpoczęcia kolejnej, tak więc "Dziewczyna o perłowych włosach"  dołączyła do kołowej listy piosenek śpiewanych "lalaaaa, nananaa, uuuu, oł jeah!"  zamiast prawdziwych słów. Zdaje się, że (oczywiście ze względu na późną porę) publika w obu przypadkach zrozumiałaby tyle samo z tego, co tak naprawdę jest śpiewane.
</p>
<br>
<p>
W trakcie całego wieczoru zostało rozegranych mnóstwo rozgrywek, z czasem coraz mniej wymagających myślenia (w jednej z ostatnich planszówek głównym zadaniem było klaskanie). Niestety niektórym (w tym i prezes) nawet i to już nie wychodziło. Pozostało zatem puszczanie baniek mydlanych i zabawa w ich łapanie (tak, tak, członkowie Koła są teoretycznie pełnoletni)...
</p>
<br>
<p>
Spotkanie można było również wykorzystać jako szansę na nadrobienie zaległości i rozmowę z osobami, których się dawno nie widziało i za rzadko nas odwiedzają (tak, Paluina, to o Tobie! ;) ).
</p>
<br>
<p> 
Czas w Cafe Piast wszystkim minął miło i szybko. Kończąc zabawę część kołowiczek postanowiła jeszcze zahaczyć o najbliższego McDonald'sa. Jednak trafienie do takowego okazało się nad wyraz trudne, gdyż pierwszym, co było słychać po wejściu do samochodu było: "gdzie jest Kraków?", a następnie, po kilku minutach "o, mam Małopolskę!". Udało się dotrzeć jadąc "na czuja". Nie udało się jednak zamówić wszystkiego gdyż, jak to skwitowała pani z obsługi "chyba chodził o o ofertę KFC - McDonald's nie ma w ofercie skrzydełek z kurczaka".
</p>
<br>
<p>
Spotkanie okazało się jednym z najlepszych spotkań integracyjnych w ostatnich latach. Dla niektórych członków Koła co prawda skończyło się w niejasnych okolicznościach, jednak zdecydowanie można je uznać za udane. Następne na celowniku, to spotkanie opłatkowe. Oby z taką samą frekwencją! 
</p>
