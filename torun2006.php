<h3>Ogólnopolskie Seminarium Studentów Astronomii III - Toruń 2006</h3>
<span class="data">Autor notki: Beata Pasternak & Janusz Nicewicz</span><br><br>

<p class="in">Ogólnopolskie Seminarium Studentów Astronomii w&nbsp;Toruniu
rozpoczęło się przyjazdem uczestników 19&nbsp;IX&nbsp;2006. W&nbsp;semianrium wzięli udział
studenci z &nbsp;Zielonej Góry, Wrocławia, Warszawy, Torunia, Szczecina, Poznania oraz Krakowa.
Pierwszego dnia wykłady wygłosili dla nas:
<ul style="margin-left: 50px;">
<li>dr Andrzej Marecki "Efemeryczny charakter aktywności radiowo głośnych AGN-ów"</li>
<li>dr hab. Michał Hanasz "Dynamo galaktyczne napędzane promieniowaniem kosmicznym"</li>
<li>dr hab. Bronisław Rudak "Gwiazdy neutronowe - unikalne laboratoria fizyczne i&nbsp;astrofizyczne"</li>
</ul>
<p>oraz studenci:
<ul style="margin-left: 50px;">
<li>Cezary Migaszewski z&nbsp;UMK w&nbsp;Toruniu "Dynamika wiekowa układu planetarnego wokół HD&nbsp;12661"</li>
<li>Andrzej Szary z&nbsp;UZ w&nbsp;Zielonej Górze "Powierzchniowe (nie-dipolowe) pola magnetyczne gwiazdy nowej: podejście ogólne i&nbsp;konsekwencje obserwacyjne".</li>
</ul>
<p>Po wyczerpującym dniu wykładów, dla relaksu, rozegraliśmy towarzyski mecz siatkówki plażowej.
<p class="in">Drugiego dnia seminarium wysłuchaliśmy następujących wykładów:
<ul style="margin-left: 50px;">
<li>prof. dr hab. Jacek Krełowski "Czego nas pan Doppler nauczył o&nbsp;ośrodku międzygwiazdowym ?"</li>
<li>dr hab. Andrzej Niedzielski "Poszukiwanie planet przy czerwonych olbrzymach"</li>
<li>dr hab. Krzysztof Goździewski "Algorytmy genetyczne w zastosowaniach astronomicznych"</li>
<li>dr hab. Maciej Konacki "Jak się szuka planet ?"</li>
<li>doc. dr hab. Ryszard Szczerba "Astrofizyka w&nbsp;podczerwieni i&nbsp;zakresie submilimetrowym - pył i&nbsp;molekuły we Wszechświecie"</li>
</ul>
<p>oraz referatów wygłoszonych przez studentów:
<ul style="margin-left: 50px;">
<li>Izabela Kowalska z&nbsp;UW w&nbsp;Warszawie "Badanie geometrii ośrodka rentgenowskiego"</li>
<li>Oliwia Madej z&nbsp;UWr we Wrocławiu "GSC&nbsp;4232.2830 gwiazda zmienna zaćmieniowa o&nbsp;zmiennej inklinacji".</li>
</ul>
<p>Wieczorem zwiedzaliśmy miasto Toruń i&nbsp;jego liczne osobliwości, w&nbsp;których można się zatracić.

<p class="in">Atrakcją ostaniego dnia seminarium była wizyta w&nbsp;Obserwatorium Astronomicznym
UMK w&nbsp;Piwnicach. Po Katedrze Radioastronomii CA&nbsp;UMK oprowadzał nas mgr Eugeniusz Pazderski.
Zwiedziliśmy 32m radioteleskop RT-4 oraz sterownię. Następnie przenieśliśmy się do Katedry
Astronomii i&nbsp;Astrofizyki CA UMK gdzie oprowadzał nas dr Maciej Mikołajewski. Obejrzeliśmy
90cm teleskop Schmidta - Cassegraina, 60cm  teleskop fotometryczny oraz historyczny
teleskop Drapera. Nie samą nauką żyje student astronomii - dlatego na zakończenie
Seminarium odbyło się ognisko, w&nbsp;którym oprócz studentów wzięli udział pracownicy obserwatorium.
Ogólnopolskie Seminarium Studentów Astronomii OSSA 2006 zakończyło się 23&nbsp;września wyjazdem
wszystkich uczestników.
