<p>Obok nowego radioteleskopu rośnie dziś okazały
jesion-samosiejka, pod którym zwracał dawniej naszą uwagę
piaskowcowy słupek. Wyryte na nim były dobrze jeszcze widoczne
litery KKE (<i>Kaiserlich Koenigliches Eigentum</i>) a poniżej
- liczba 15. Słupek ten wyznaczał granicę dawnego
fortu-twierdzy. Lecz teraz nie wyznacza już niczego. W wyniku
prowadzonych prac przy radioteleskopie, właśnie ten 15-ty
musiał być usunięty; dokładniej - wykopany i, ze względu
na swój ciężar, przerzucony w&nbsp;bok od swego dawnego miejsca.
Można go było podziwiać w całej krasie do czasu, gdy nie
został zabetonowany, użyty do wzmocnienia fundamentu płyty
chodnikowej przy schodach wiodących do kabiny radioteleskopu,
gdzie nastąpiło uroczyste przecięcie wstęgi w dniu 26
października 1995.</p><br>

<p>Zapytacie zapewne: - I cóż z tego? Moim zdaniem (a
człowiekowi najrozmaitsze myśli chodzą po głowie) nie można
wierzyć w przesądy. W końcu zagadkowy ten kamień KKE 15 nie
może mieć żadnego wpływu na resetowanie się radioteleskopu!
Zwyciężyć musi zdrowy rozsądek, a kolejna, rzekomo
niewytłumaczalna, tajemnica Fortu nr 38 winna ujrzeć światło
dzienne. Ale czy to faktycznie już koniec zagadek?</p><br>

<p>Przy ścieżce, na terenie obserwatoryjnego fortu, nie ma już
słupka, niemego świadka wydarzeń z ponad stuletniego okresu
historii fortu, z wyrytymi nań literami i cyframi KKE 15. Musimy
go chyba koniecznie wkopać, na stare miejsce. Z czasem może
znów siądzie kiedyś na nim Brutus z obróżką
antykleszczową, wypatrując buszujących myszek w listowiu, a
radioteleskop włączony w sieć komputerową przestanie się
resetować.</p><br>
