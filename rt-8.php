<h3>Postawienie Małego Radioteleskopu, 26. października 1995 rok.</h3>
<span class="data">Autor: Adam Michalec, Acta Brutusica 1996, 24 maja 1996</span><br><br>

<p align="left">Nowy radioteleskop, z czaszą 8-metrowej
średnicy wspartą na pięciu podporach, ponad budką
obserwacyjną, uroczyście otwarto w Obserwatorium Astronomicznym
UJ na Forcie Skała w dniu 26 października 1995 r. Świadkami
tej uroczystości byli licznie zaproszeni goście z całej
Polski, a nawet z USA.</p><br>

<p>- Słońce doceniło dzisiejszą uroczystość -
powiedział JM Rektor Uniwersytetu Jagiellońskiego, prof.
Aleksander Koj, mając na myśli piękną jesienną pogodę,
która towarzyszyła oficjalnemu oddaniu do użytku nowego
radioteleskopu. Nikt nie przypuszczał wtedy, że już 8 dni
później, ten nowoczesny instrument będzie musiał poddać się
długiej, ostrej zimowej weryfikacji - i to aż do 20 kwietnia
1996.</p><br>

<p>Paraboloid ten zastąpił starą i wysłużoną 40-letnią,
7-metrową antenę, którą praktycznie zjadła już rdza. Nowy
jest w pełni zautomatyzowany i nie wymaga - przeciwnie niż
jego poprzednik - ciągłej obsługi przez człowieka. "Budzi
się" sam po&nbsp;wschodzie Słońca, śledzi jego ruch notując
poziom radiopromieniowania, a&nbsp;z&nbsp;chwilą nadejścia nocy wyłącza
się przechodząc w stan uśpienia. Dzięki temu czas, który
pochłaniało kontrolowanie pracy dotychczasowego przyrządu,
można teraz poświęcić na opracowywanie danych, które są
następnie udostępniane w sieci komputerowej.</p><br>

