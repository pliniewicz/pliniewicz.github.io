<p>Obserwacje radiowe Słońca zostały po raz pierwszy
przeprowadzone jeszcze w czasie trwania II wojny światowej, a
kilka lat po jej zakończeniu, w&nbsp;paru ośrodkach na świecie,
rozpoczęto systematyczną, codzienną tzw. radiową służbę
Słońca.</p><br>

<p>W Krakowie, na Forcie Skała, służba ta rozpoczęła się 1
października 1957 r. obserwacjami na fali decymetrowej o
długości 37 cm (810 MHz), do których w roku 1966 dodano
obserwacje na fali ok. 70 cm (410 MHz). Do obserwacji
wykorzystywano radioteleskop zbudowany systemem gospodarczym
jeszcze w 1953 r., którego jedynym przeznaczeniem miało być
przeprowadzenie radiowych obserwacji całkowitego zaćmienia
Słońca w dniu 30 czerwca 1954 r. Inicjatorem tego projektu był
wieloletni dyrektor Obserwatorium, prof. Tadeusz Banachiewicz,
zaś wykonawcami pionierzy radioastronomii w naszym kraju: Adam
Strzałkowski&nbsp;i&nbsp;Oleg Czyżewski. Po trzech latach, które
minęły od owego zaćmienia, rdzewiejący, przeznaczony do
jednorazowego użytku instrument, został przystosowany do
ciągłej pracy obserwacyjnej i rozpoczął swoją prawie
czterdziestoletnią służbę. Stało się to możliwe dzięki
funduszom uzyskanym z okazji III Międzynarodowego Roku
Geofizycznego i pełnemu zaangażowaniu Olega Czyżewskiego i
Józefa Masłowskiego w budowę nowej aparatury pomiarowej.</p><br>

<p>Ale w ostatnich latach stan techniczny radioteleskopu zaczął
się gwałtownie pogarszać, grożąc w każdej chwili
przerwaniem wieloletniej serii obserwacyjnej. Z&nbsp;inicjatywy prof.
Józefa Masłowskiego, Kierownika Zakładu Radioastronomii i
Fizyki Kosmicznej, podjęto więc przygotowania do modernizacji
systemu. Opracowano kompleksowy plan niezbędnych zmian, dokonano
rozpoznania cenowego i wystąpiono do Komitetu Badań Naukowych o
stosowne fundusze, które uzyskano w sierpniu 1993 r.</p><br>

<p>Modernizacja była gruntowna. Nowy radioteleskop stał się
instrumentem całkowicie zautomatyzowanym, wyposażonym w
wysokiej klasy urządzenia odbiorcze najnowszej generacji, a
więc przyrządem na poziomie światowym. Przeznaczeniem
radioteleskopu jest pomiar strumienia promieniowania radiowego
Słońca w zakresie fal decymetrowych, z możliwością
wykonywania okresowej kalibracji poprzez obserwacje źródeł
standardowych. Umożliwi to precyzyjne wyznaczanie codziennego
widma Słońca w badanym zakresie fal.</p><br>

<p>Jego powstanie było możliwe dzięki sprawnej współpracy
krakowskich radioastronomów, m.in. dr Andrzeja Kułaka, prof.
Jerzego Machalskiego, dr&nbsp;Stanisława Zięby i innych,
działających pod kierunkiem prof. Józefa Masłowskiego, z&nbsp;zespołem głównego wykonawcy tj. Zakładu Aparatury Naukowej
UJ. Nowa, 8-metrowa antena została wykonana w tym Zakładzie
według projektu i pod nadzorem mgr inż. Andrzeja Szula, który
zastosował w konstrukcji wiele całkowicie nowych -
chronionych patentami - rozwiązań, zgodnie z naszymi
założeniami technicznymi.</p><br>

<p>W Wielki Czwartek, 13 kwietnia 1995 r., antena została
przetransportowana helikopterem z Zakładu Aparatury na Skałę,
a na początku lipca przekazana Obserwatorium do przeprowadzenia
szeregu koniecznych testów technicznych. Do&nbsp;dnia 19
października obserwacje Słońca prowadzano nowym (do godz. 12) i&nbsp;starym (od godz.12) radioteleskopem (inaczej przeszkadzałyby
sobie wzajemnie) celem nawiązania obu serii obserwacyjnych. W
tym też dniu rozpoczęto demontaż 7-metrowej anteny, który
trwał trzy dni. Przeprowadzone niezbędne testy nowej anteny
wykazały, że nowy instrument jest w pełni sprawny i powinien
zapewnić wysoką jakość obserwacji. Cieszyć się zatem
należy, że dzięki wysiłkowi całego Zespołu, kierowanego
przez Dyrektora OA UJ prof. Józefa Masłowskiego, zdążyliśmy
ze wszystkim na czas i mogło dojść do przecięcia symbolicznej
wstęgi (niestety oficjalnie nie doszło do rozbicia butelki
szampana mimo wcześniej przeprowadzonych prób). W czwartek 26
października 1995 r. o godz. 12<sup>20 </sup>nowy radioteleskop
oficjalnie ruszył do "służby" witając świadków tego
zdarzenia mile dźwięczącym gongiem. Gong ten - jak się
później okazało - był sygnałem do licznych naszych
"starć" z nowym instrumentem, który ujawnił swoisty
charakterek.</p><br>

<p>Po oficjalnej uroczystości 26 października licznie zebrani
Goście zostali zaproszeni na&nbsp;wspaniały lunch, który odbył
się w dworku w Modlnicy, nam zaś został oddany w&nbsp;ręce
przyrząd do badania Słońca, którego badanie jest dziedziną
astronomii bezpośrednio wiążącą się z naszymi ziemskimi
sprawami.</p><br>

<p>Zgodnie z założeniami pliku konfiguracyjnego programu
"Sol", teleskop czuwa w&nbsp;pozycji zaparkowanej, aż do momentu
gdy ma rozpocząć obserwacje. Od&nbsp;15&nbsp;listopada 95 obserwacje są
wykonywane w dwóch blokach czasowych na&nbsp;8&nbsp;kanałach, a od 1
marca 1996 - na 11 kanałach.</p><br>

<p>Jak już wspomniałem, radioteleskop pokazał swój
charakterek - po raz pierwszy 20&nbsp;listopada 95, kiedy to
wystąpiło oblodzenie napędu radioteleskopu. Potem nastąpiły
wydarzenia grudniowe: 18 grudnia o godz. 3<sup>28 </sup>UT
zbuntował się; chcąc chyba wyrwać się z więzów programu w
czasie czuwania, o tej osobliwej porze rozpoczął poszukiwanie
Słońca! Nie trudno sobie wyobrazić, że wynikła z tego awaria
napędu. Teleskop zatrzymał się na wyłączniku krańcowym,
będąc w południku na <font face="Symbol">d</font>&nbsp;=&nbsp;46&nbsp;<font
face="Symbol">°</font>&nbsp;58'. Z powodu niesprzyjającej aury
usuwanie awarii trwało do 23 grudnia i w Wigilię teleskop
wystartował. Zapanowała odwilż, Boże Narodzenie było dniem
wolnym od obserwacji, zaś w noc na św. Szczepana spadł duży
śnieg i rano ochłodziło się do <font face="Symbol">-</font>
15 C. Teleskop zamarł w okowach lodu i mimo naszych wysiłków
trwał tak aż do&nbsp;11&nbsp;stycznia 1996. Dopiero 12 stycznia '96
antena ruszyła ponownie do służby, nie wykazując na
szczęście nowych niespodzianek w napędzie. Tajemniczą sprawą
są jednak resetowania programu, które czekają na wyjaśnienie
i&nbsp;ich usunięcie.</p><br>

