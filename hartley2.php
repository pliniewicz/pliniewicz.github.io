<h3>Kometa 103P/Hartley 2</h3>
<span class="data">Autor: Sebastian Kurowski, 30 listopada 2010 r.</span><br><br>
<p>&nbsp;&nbsp;&nbsp;Komety są to ciała niebieskie, od czasu do czasu nawiedzające okolice Słońca. Krążą po orbitach parabolicznych, hiperbolicznych (komety nieokresowe) lub&nbsp;rozciągniętych orbitach eliptycznych (komety okresowe). W silnym uproszczeniu kometę można porównać do kuli brudnego śniegu, która zbliżając się do Słońca zaczyna parować, co zwykle powoduje powstanie pięknego warkocza.
</p>
<br><p>
&nbsp;&nbsp;&nbsp;W zamierzchłych czasach komety traktowano jako omen: nadciągając, miały oznajmiać różnego rodzaju klęski żywiołowe, kataklizmy, wojny, choroby itp. Obecnie (z&nbsp;niewyjaśnionych przyczyn ;) ) komety złagodniały i ich wizyty w okolicach Ziemi głównie roznoszą dobrą nowinę wśród astronomów-amatorów, żądnych nowego obiektu do obserwacji. W końcu wszelkiego rodzaju mgławice, galaktyki czy nawet planety, są praktycznie niezmienne w porównaniu z nieprzewidywalnymi kometami.
</p>
<br><p>
&nbsp;&nbsp;&nbsp;Łaski usadowienia fotonów w naszym Maksutowie doznała niedawno doskonale widoczna, kometa krótkookresowa 103P/Hartley 2. Okres orbitalny komety wynosi około 6,46 lat, maksymalne zbliżenie do Ziemi nastąpiło 20 października 2010 roku, natomiast 28 października kometa znalazła się w peryhelium.
</p>
<br><p>
&nbsp;&nbsp;&nbsp;Poniższe zdjęcie jest złożeniem dziesięciu klatek, po 30 sekund każda, wykonanych w nocy 4/5 października 2010 r. aparatem Canon EOS 350D, w ognisku głównym 35-centymetrowego teleskopu maksutowa.<br><br>
</p>

