<h3>II Astrominarium NKSA</h3>
<span class="data">Autor: Bartłomiej Dębski, 18 grudnia 2010 r.</span><br><br>

<p>
&nbsp;&nbsp;&nbsp; Dnia 16.12.2010 r. o godzinie 19:30 rozpoczęło się II Astrominarium NKSA. Na&nbsp;czwartkowym spotkaniu stawiły się 23 osoby (trzy doszły w trakcie seminarium). Projekty z rodziny Zooniverse zreferowała Marta Kotarba.
</p>
<br>
<p>
&nbsp;&nbsp;&nbsp; Podczas referatu Marta Kotarba przybliżyła zebranym kilka projektów, które wyewoluowały z Galaktycznego Zoo do miana projektów autonomicznych. Projektami tymi były m.im.: Old Weather (próba odtworzenie pogody z przeszłości bazując na&nbsp;zapiskach w dziennikach kapitańskich Marynarki Wielkiej Brytanii i in.), Galaxy Zoo -&nbsp;Mergers (pomoc w dobieraniu właściwych modeli numerycznych ewolucji galaktych w&nbsp;fazie kolizji), Galaxy Zoo - Hunt For Supernovae (poszukiwanie kandydatów na&nbsp;supernowe bazując na obrazach różnicowych z teleskopu Hubble'a) oraz Milky Way Project (zaznaczanie charakterystycznych obiektów na mapie nieba w zakresie dysku Galaktyki).
</p>
<br>
<p>
&nbsp;&nbsp;&nbsp; Oprócz powyższe wspomniane zostały takie projekty, jak: Moon Zoo, Solar&nbsp;Stormwatch, Galaxy Zoo Hubble, a także Planethunters, które zostało zainicjowane w kilka godzin przed rozpoczęciem II Astrominarium. Przybliżona została też&nbsp;społeczność biorących udział w projektach. Zaprezentowano forum internetowe, miejsca dyskusji oraz liczne blogi tematyczne.
</p>
<br>
<p>
&nbsp;&nbsp;&nbsp; Astrominarium trwało do 21:20, przy czym rozwinęło sie później w luźną dyskusję trwającą kolejne kilkanascie minut. Po&nbsp;zebraniu zostały przypomniane najwazniejsze sprawy związanie z NKSA w najbliższym czasie (jak np. spotkanie opłatkowe), po&nbsp;czym udano się na obserwacje nieba.
</p>
