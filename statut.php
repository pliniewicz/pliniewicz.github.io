<h3>Statut Naukowego Koła Studentów Astronomii</h3>

<p class="art">I. Nazwa i siedziba stowarzyszenia</p>
<p style="text-align: left">
<span class="nr">§1</span>. Stowarzyszenie nosi nazwę "Naukowe Koło Studentów Astronomii" (dalej zwane Kołem).<br/>
<span class="nr">§2</span>. Siedzibą Koła jest miasto Kraków.<br/>
<span class="nr">§3</span>. Koło działa przy Uniwersytecie Jagiellońskim.<br/>
<span class="nr">§4</span>. Koło działa zgodnie ze statutem Koła, statutem UJ i prawem polskim.<br/>
<span class="nr">§5</span>. Opiekę nad Kołem sprawuje Dyrekcja Obserwatorium za pośrednictwem Opiekuna, który jest wybierany na wniosek członków Koła.
<p class="art">II. Zadania oraz forma pracy Koła</p>
<p style="text-align: left">
<span class="nr">§6</span>. Do celów Koła należy:</p>
<ul style="margin-left: 50px;">
	<li>rozbudzanie wśród studentów zainteresowań naukowych i pasji badawczych;</li>
	<li>poszerzanie horyzontów i zainteresowań studentów;</li>
	<li>promowanie osiągnięć naukowych członków Koła;</li>
	<li>przygotowanie studentów do przyszłej pracy naukowej;</li>
	<li>integracja studentów i pracowników naukowych różnych wydziałów UJ;</li>
	<li>popularyzacja nauk przyrodniczych ze szczególnym uwzględnieniem astronomii.</li>
</ul>
<p style="text-align: left">
<span class="nr">§7</span>. Koło realizuje swe cele poprzez:
<ul style="margin-left: 50px;">
	<li>współpracę z innymi organizacjami studenckimi, kołami oraz instytucjami naukowymi;</li>
	<li>organizowanie odczytów, zebrań i dyskusji popularyzujących badania łączące zagadnienia z różnych dziedzin nauki;</li>
	<li>umożliwianie studentom publikacji ich prac naukowych;</li>
	<li>udział w seminariach, odczytach i konferencjach;</li>
	<li>wspieranie współpracy międzywydziałowej;</li>
	<li>koleżeńską pomoc w zrozumieniu wybranych zagadnień naukowych;</li>
	<li>organizowanie zebrań, wyjazdów i innych spotkań towarzyskich.</li>
</ul>
<p class="art">III. Władze Koła</p>
<p style="text-align: left">
<span class="nr">§8</span>. Władzami Koła są: Walne Zgromadzenie i Zarząd.<br/>
<span class="nr">§9</span>. Walne Zgromadzenie będące zgromadzeniem zwyczajnych członków Koła ma prawo uchwalania Statutu oraz regulacji ogólnych zasad działania Koła oraz wyboru Zarządu.<br/>
<span class="nr">§10</span>. Informacja o terminie Walnego Zgromadzenia musi być podana nie później niż na tydzień przed jego wyznaczoną datą.<br/>
<span class="nr">§11</span>. Walne Zgromadzenie może być zwyczajne lub nadzwyczajne.<br/>
<span class="nr">§12</span>. Zwyczajne Walne Zgromadzenie odbywa się dwa razy w roku akademickim. Pierwsze spotkanie nie później niż 30 listopada, a drugie nie później niż 31 maja (wybór zarządu).<br/>
<span class="nr">§13</span>. Nadzwyczajne Walne Zgromadzenie może być zwołane na wniosek Zarządu lub zwyczajnych członków Koła.<br/>
<span class="nr">§14</span>. Walnemu zgromadzeniu przewodniczy prezes lub osoba przez niego wyznaczona.<br/>
<span class="nr">§15</span>. Do prawomocności posiedzenia Walnego Zgromadzenia niezbędna jest obecność co najmniej połowy
	zwyczajnych członków Koła. Jeśli z powodu zbyt niskiej frekwencji zebranie nie dojdzie do skutku, to zebranie zwołane w drugim
	terminie jest prawomocne bez względu na liczbę obecnych członków.<br/>
<span class="nr">§16</span>. Uchwały dotyczące zmian statutu i rozwiązania Koła podejmowane są większością 2/3 głosów.<br/>
<span class="nr">§17</span>. Pozostałe uchwały Walne Zgromadzenie podejmuje zwykła większością głosów.<br/>
<span class="nr">§18</span>. Walne Zgromadzenie ustala plan pracy Koła.<br/>
<span class="nr">§19</span>. W skład Zarządu wchodzą:
<ul style="margin-left: 50px;">
	<li>Prezes;</li>
	<li> Wiceprezes;</li>
	<li>Sekretarz.</li>
</ul>
<p style="text-align: left">
<span class="nr">§20</span>. Zarząd wybierany jest przez Walne Zgromadzenie w tajnym głosowaniu (dopuszczalne jest także jawne).<br/>
<span class="nr">§21</span>. Zarząd zobowiązany jest do realizacji planu pracy ustalonego przez Walne Zgromadzenie.<br/>
<span class="nr">§22</span>. Zarząd rozporządza finansami i prowadzi rachunkowość Koła.<br/>
<span class="nr">§23</span>. Prezes jest odpowiedzialny za całokształt prac Zarządu.<br/>
<p class="art">IV. Zasady członkostwa w Kole</p>
<p style="text-align: left">
<span class="nr">§24</span>. Członkiem Koła może zostać każdy student Astronomii na Wydziale Fizyki, Astronomii i Informatyki Stosowanej
Uniwersytetu Jagiellońskiego, po uprzedniej zgodzie Zarządu.<br />
<span class="nr">§25</span>. Osoby spoza grona studentów Astronomii mogą uzyskać członkostwo po zatwierdzeniu przez Walne Zgromadzenie.</p>
