<h3>Wyjazd do MOA w Niepołomicach 2010</h3>
<span class="data">Autor notki: Agnieszka Świętoń & Patrycja Pakońska</span><br><br>
<p class="in" align="justify">Wyjazd do Młodzieżowego Obserwatorium w Niepołomicach rozpoczął się 22 maja 2010 roku. Wzięli w nim udział członkowie Naukowego Koła Studentów Astronomii UJ.</p>

<p class="in"align="justify">Zapoznaliśmy się z przygotowanym do otwarcia planetarium. Pomagaliśmy w testowaniu aparatury. Uczestniczyliśmy w próbnym seansie. Pan Grzegorz Sęk wygłosił prezentację związaną z tematyką galaktyk.</p>


<p class="in" align="justify">Skorzystaliśmy również z boiska, które znajdowało się przy obserwatorium. Zagraliśmy towarzyski mecz w piłkę nożną. Wieczorem upiekliśmy na grillu kiełbaski, które zjedliśmy w towarzystwie pana Grzegorza. Kolacji towarzyszyła ożywiona dyskusja dotycząca propozycji tematyki seansów w planetarium.</p>
